// JavaScript Math Objects examples:

// Math.PI example:
console.log(Math.PI);

// Math.round() example  (rounds to the nearest integer):
console.log(Math.round(4.7));

// Math.pow() example (returns the value of x to the power of y):
console.log(Math.pow(8, 2));

// Math.sqrt() example (returns the square root of a number):
console.log(Math.sqrt(64));

// Math.abs() example (returns the absolute value of a number):
console.log(Math.abs(-4.7));

// Math.ceil() example (rounds a number up to the nearest integer):
console.log(Math.ceil(4.4));

// Math.floor() example (rounds a number down to the nearest integer):
console.log(Math.floor(4.7));

// Math.min() example (returns the number with the lowest value):
console.log(Math.min(0, 150, 30, 20, -8, -200));

// Math.max() example (returns the number with the highest value):
console.log(Math.max(0, 150, 30, 20, -8, -200));

// Math.random() example (returns a random number between 0 and 1):
console.log(Math.random());

// Math.trunc() example (returns the integer part of a number):
console.log(Math.trunc(4.7));

// Math.sign() example  (returns 1 if positive, -1 if negative, 0 if zero)
console.log(Math.sign(-4.7));

// Math.sin() example (in radians)
console.log(Math.sin((90 * Math.PI) / 180));

// Math.cos() example  // 1
console.log(Math.cos((0 * Math.PI) / 180));

// Math.tan() example (tan(90) is undefined)
console.log(Math.tan((45 * Math.PI) / 180));

// Math.asin() example (returns the arcsine of a number)
console.log(Math.asin((1 * Math.PI) / 180));

// Math.acos() example  (returns the arccosine of a number)
console.log(Math.acos((1 * Math.PI) / 180));

// Math.atan() example (in radians)
console.log(Math.atan((1 * Math.PI) / 180));

// Math.log() example  (base e)
console.log(Math.log(2));

// Math.log10() example (base 10)
console.log(Math.log10(2));

// Math.log2() example (log base 2)
console.log(Math.log2(2));

// Math.expm1() example (e^x - 1)
console.log(Math.expm1(1));

// Math.cbrt() example (cube root)
console.log(Math.cbrt(8));

// Math.hypot() example (returns the square root of the sum of squares of its arguments)
console.log(Math.hypot(3, 4));

// Math.imul() example (returns the result of the C-like 32-bit multiplication of the two parameters)
console.log(Math.imul(2, 4));

// Math.clz32() example (returns the number of leading zero bits in the 32-bit binary representation of a number)
console.log(Math.clz32(0));

// Math.fround() example (returns the nearest single precision float representation of a number)
console.log(Math.fround(0.1));

// Math.sinh() example (hyperbolic sine)
console.log(Math.sinh(1));

// Math.cosh() example (hyperbolic cosine)
console.log(Math.cosh(1));

// Math.tanh() example (hyperbolic tangent)
console.log(Math.tanh(1));

// Math.asinh() example (hyperbolic arcsine)
console.log(Math.asinh(1));

// Math.acosh() example (hyperbolic arccosine)
console.log(Math.acosh(1));

// Math.atanh() example (hyperbolic arctangent)
console.log(Math.atanh(1));
