// javscript data types examples
// 1. Number
var num = 10
console.log(num)
// 2. String
var str = 'Hello World'
console.log(str)
// 3. Boolean
var bool = true
console.log(bool)
// 4. Null
var n = null
console.log(n)
// 5. Undefined
var u = undefined
console.log(u)
// 6. Array
var arr = [1, 2, 3, 4, 5]
console.log(arr)
// 7. Object

var obj = {
  name: 'John',
  age: 25,

  address: {
    city: 'New York',
    state: 'NY',
  },
}

console.log(obj)
